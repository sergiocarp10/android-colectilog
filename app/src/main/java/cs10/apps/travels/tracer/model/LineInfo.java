package cs10.apps.travels.tracer.model;

public class LineInfo {
    private Integer line;
    private int count;

    public Integer getLine() {
        return line;
    }

    public void setLine(Integer line) {
        this.line = line;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
